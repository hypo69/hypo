import random

# פונקציה המדמה הטלת קובייה
def roll_dice():
    """
    מדמה הטלת קובייה בעלת שש פאות.
    מחזירה מספר אקראי בין 1 ל-6.
    """
    # יצירת מספר אקראי בין 1 ל-6
    dice_roll = random.randint(1, 6)
    # החזרת המספר האקראי
    return dice_roll

# תחילת המשחק: הטלת הקובייה
def play_dice_game():
    """
    מפעילה את המשחק.
    מדפיסה את תוצאת הטלת הקובייה.
    """
    # קריאה לפונקציה שמטילה את הקובייה ושמירת התוצאה
    result = roll_dice()
    # הדפסת התוצאה למשתמש
    print(f"התוצאה של הטלת הקובייה היא: {result}")

# הפעלת המשחק רק כאשר הקובץ מורץ ישירות
if __name__ == "__main__":
    play_dice_game()

