import random

def roll_dice():
    """
    Имитирует бросок двух шестигранных костей.

    Возвращает:
    int: Сумма очков двух костей.
    """
    die1 = random.randint(1, 6) # генерируем случайное число от 1 до 6 для первой кости
    die2 = random.randint(1, 6) # генерируем случайное число от 1 до 6 для второй кости
    return die1 + die2 # возвращаем сумму очков двух костей

def craps():
    """
    Запускает игру в кости "Крэпс".
    """
    print("Игра Крэпс началась!")
    
    # Первый бросок
    first_roll_sum = roll_dice() # бросаем кости первый раз
    print(f"Выпало: {first_roll_sum}") # выводим результат первого броска

    # Проверка первого броска
    if first_roll_sum in (7, 11): # если выпало 7 или 11, то выигрыш
        print("Вы выиграли!")
        return
    elif first_roll_sum in (2, 3, 12): # если выпало 2, 3 или 12, то проигрыш
        print("Вы проиграли!")
        return
    else:
        # Установка "пойнта"
        point = first_roll_sum # запоминаем сумму первого броска как "пойнт"
        print(f"Теперь ваша цель - выбросить {point} до 7.") # выводим сообщение о установленном "пойнте"

    # Продолжение игры до выигрыша или проигрыша
    while True:
        current_roll_sum = roll_dice() # делаем новый бросок
        print(f"Выпало: {current_roll_sum}") # выводим результат броска

        if current_roll_sum == point: # если выпал "пойнт", то выигрыш
            print("Вы выиграли!")
            return
        elif current_roll_sum == 7: # если выпало 7, то проигрыш
            print("Вы проиграли!")
            return
# Запуск игры
craps()
