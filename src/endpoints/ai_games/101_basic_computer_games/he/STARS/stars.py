import random
import os

# הגדרת משתנה Y לספירת השורות ואתחולו ל-0
Y = 0
# הגדרת משתנה S, המחרוזת שתכיל את הכוכבים והרווחים, ואתחולו למחרוזת ריקה
S = ''

# לולאה חיצונית שתרוץ 24 פעמים, כל פעם להדפסת שורה
while Y < 24:
    # אתחול S כמחרוזת ריקה בכל שורה
    S = ''
    # לולאה פנימית שתרוץ 6 פעמים, בכל פעם לבחירת תו להדפסה (כוכב או רווח)
    for _ in range(6):
        # יצירת מספר אקראי בין 0 ל-63
        X = random.randint(0, 63)
        # אם X קטן מ-31, הוסף רווח למחרוזת S
        if X < 31:
            S = S + ' '
        # אחרת, הוסף כוכבית למחרוזת S
        else:
            S = S + '*'
    # הדפס את המחרוזת S (שורת הכוכבים)
    print(S)
    # הגדל את Y ב-1 לקראת השורה הבאה
    Y += 1

# בקשת קלט מהמשתמש כדי לעצור את התוכנית
input("הקש על מקש כלשהו כדי לסיים...")

