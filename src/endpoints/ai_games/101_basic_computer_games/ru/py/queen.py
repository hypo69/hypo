"""
QUEEN:
=================
Сложность: 4
-----------------
Игра "Ферзь" - это простая текстовая игра, в которой компьютер расставляет ферзя на шахматной доске размером 8x8 и предлагает игроку угадать координаты ферзя. Игрок вводит координаты в формате "ряд, столбец", и компьютер сообщает, правильно ли угадана позиция или нет. Игра продолжается до тех пор, пока игрок не угадает позицию ферзя.

Правила игры:
1. Компьютер случайным образом размещает ферзя на шахматной доске 8x8.
2. Игрок вводит координаты предполагаемой позиции ферзя в формате "ряд, столбец".
3. Компьютер проверяет, совпадает ли введенная позиция с фактической позицией ферзя.
4. Если позиции совпадают, игра заканчивается, игрок побеждает.
5. Если позиции не совпадают, игрок продолжает попытки.
-----------------
Алгоритм:
1.  Сгенерировать случайные координаты ряда и столбца в диапазоне от 1 до 8 для размещения ферзя.
2.  Начать цикл "пока ферзь не угадан":
    2.1 Запросить у игрока ввод предполагаемых координат ряда и столбца.
    2.2 Если введенные координаты совпадают с координатами ферзя, вывести сообщение "YOU GOT IT", завершить игру.
    2.3 Если введенные координаты не совпадают с координатами ферзя, вернуться к шагу 2.1.
-----------------
Блок-схема:
```mermaid
flowchart TD
    Start["Начало"] --> InitializeQueenPosition["<p align='left'>Инициализация позиции ферзя:
    <code><b>
    queenRow = random(1, 8)
    queenCol = random(1, 8)
    </b></code></p>"]
    InitializeQueenPosition --> LoopStart{"Начало цикла: пока позиция не угадана"}
    LoopStart --> InputPosition["Ввод позиции пользователем: <code><b>userRow, userCol</b></code>"]
    InputPosition --> CheckPosition{"Проверка: <code><b>userRow == queenRow и userCol == queenCol?</b></code>"}
    CheckPosition -- Да --> OutputWin["Вывод сообщения: <b>YOU GOT IT</b>"]
    OutputWin --> End["Конец"]
    CheckPosition -- Нет --> LoopStart
    End

```

Legenda:
    Start - Начало игры.
    InitializeQueenPosition - Инициализация позиции ферзя: queenRow (ряд ферзя) и queenCol (столбец ферзя) устанавливаются случайными значениями от 1 до 8.
    LoopStart - Начало цикла, который продолжается, пока позиция ферзя не будет угадана.
    InputPosition - Запрос у пользователя ввода предполагаемой позиции ферзя (userRow и userCol).
    CheckPosition - Проверка, совпадает ли введенная пользователем позиция с фактической позицией ферзя.
    OutputWin - Вывод сообщения "YOU GOT IT", если позиции совпадают.
    End - Конец игры.
"""
import random

# Генерируем случайные координаты для ферзя (от 1 до 8)
queenRow = random.randint(1, 8)
queenCol = random.randint(1, 8)

# Основной игровой цикл
while True:
    # Запрашиваем у пользователя координаты предполагаемой позиции ферзя
    try:
        userRow = int(input("Введите ряд (1-8): "))
        userCol = int(input("Введите столбец (1-8): "))
    except ValueError:
        print("Пожалуйста, введите целые числа от 1 до 8.")
        continue
    
    # Проверяем, угадал ли пользователь позицию ферзя
    if userRow == queenRow and userCol == queenCol:
        print("YOU GOT IT")
        break  # Завершаем цикл, если позиция угадана
    else:
        print("Неверно, попробуйте еще раз.")
"""
Пояснения:
1.  **Импорт модуля `random`**:
   -  `import random`: Импортирует модуль `random`, который используется для генерации случайных чисел.
2.  **Инициализация координат ферзя**:
    -   `queenRow = random.randint(1, 8)`: Генерирует случайное целое число от 1 до 8 для ряда ферзя.
    -   `queenCol = random.randint(1, 8)`: Генерирует случайное целое число от 1 до 8 для столбца ферзя.
3.  **Основной игровой цикл `while True:`**:
    -   Бесконечный цикл, который продолжается до тех пор, пока игрок не угадает позицию ферзя (будет выполнена команда `break`).
    -   **Ввод данных**:
        -   `try...except ValueError`: Блок try-except обрабатывает возможные ошибки ввода. Если пользователь введет не целые числа, то будет выведено сообщение об ошибке.
        -   `userRow = int(input("Введите ряд (1-8): "))`: Запрашивает у пользователя ввод ряда и преобразует его в целое число.
        -   `userCol = int(input("Введите столбец (1-8): "))`: Запрашивает у пользователя ввод столбца и преобразует его в целое число.
    -   **Проверка позиции**:
        -   `if userRow == queenRow and userCol == queenCol:`: Проверяет, совпадают ли введенные координаты с координатами ферзя.
        -   `print("YOU GOT IT")`: Выводит сообщение о победе.
        -   `break`: Завершает цикл (игру), если позиция угадана.
    -   **Сообщение о неверном ответе**:
        -   `else:`: Если позиция не угадана.
        -   `print("Неверно, попробуйте еще раз.")`: Выводит сообщение о неверной попытке.
"""
