"""
RUSROU:
=================
Сложность: 3
-----------------
Игра "Русская Рулетка" имитирует вращение барабана револьвера с одним патроном. 
Игрок делает выстрел, и, если "повезёт", то он остаётся "жив". 
Игра моделирует ситуацию с заряженным одним патроном из шести. 
Цель - остаться "живым" как можно дольше.

Правила игры: 
1. Игрок запускает "вращение барабана".
2. Происходит случайный выбор номера камеры барабана.
3. Если в выбранной камере есть патрон, то игра заканчивается.
4. Если в выбранной камере нет патрона, игрок продолжает игру, начиная с пункта 1.
-----------------
Алгоритм:
1. Начало игры.
2. Вывести сообщение о начале игры.
3. Запустить цикл игры.
4. Сгенерировать случайное целое число от 1 до 6, представляющее номер камеры барабана.
5. Если сгенерированное число равно 1, вывести сообщение о выстреле и завершить игру.
6. Если сгенерированное число не равно 1, вывести сообщение о том, что игрок жив.
7. Перейти к пункту 3.
-----------------
Блок-схема: 
```mermaid
  graph TD
      Start(Start) --> OutputStart("Вывести: Начало игры");
      OutputStart --> GameLoopStart("Начало цикла игры");
      GameLoopStart --> GenerateRandomNumber("Сгенерировать случайное число от 1 до 6");
      GenerateRandomNumber --> CheckIfShot("Число равно 1?");
      CheckIfShot -- Yes --> OutputShot("Вывести: Выстрел!")
      OutputShot --> End("Конец игры");
      CheckIfShot -- No --> OutputAlive("Вывести: Вы жив!");
      OutputAlive --> GameLoopStart;
```
"""
import random

def russian_roulette():
    """
    Имитирует игру в русскую рулетку.
    """
    print("Начало игры в русскую рулетку!") # Выводим сообщение о начале игры

    while True: # Запускаем бесконечный цикл игры
        chamber_number = random.randint(1, 6) # Генерируем случайное число от 1 до 6 (номер камеры барабана)

        if chamber_number == 1: # Если выпала камера с патроном (номер 1)
            print("Выстрел! Вы проиграли.") # Сообщаем о проигрыше
            break # Завершаем цикл игры
        else: # Если патрон в другой камере
            print("Вы живы! Продолжаем...") # Сообщаем что игрок выжил и продолжаем игру


if __name__ == "__main__":
    russian_roulette() # Запускаем игру

