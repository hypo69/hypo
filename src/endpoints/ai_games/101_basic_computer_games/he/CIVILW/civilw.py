import random

# הגדרת משתנים ראשוניים
playerPower = 1000 # כוח השחקן
computerPower = 1000 # כוח המחשב
playerAreas = [0, 0, 0, 0] # מערך לאחסון כוחות השחקן בכל אזור
computerAreas = [0, 0, 0, 0] # מערך לאחסון כוחות המחשב בכל אזור
area_names = ["א", "ב", "ג", "ד"] # מערך עם שמות האזורים

# קליטת כוחות השחקן לכל אזור
print("הקצה את כוחותיך לארבעת האזורים (סה'כ 1000):")
for i in range(4):
    while True:
      try:
        playerAreas[i] = int(input(f"כמה כוח תשים באזור {area_names[i]}? "))
        if playerAreas[i] >= 0 and playerAreas[i] <= playerPower:
            playerPower -= playerAreas[i]
            break
        else:
            print("קלט לא תקין. אנא הזן מספר חיובי שלא גדול ממה שנותר לך.")
      except ValueError:
        print("קלט לא תקין. אנא הזן מספר שלם.")


# המחשב מקצה כוחות באופן אקראי
remaining_power = computerPower
for i in range(4):
    if i < 3:
        computerAreas[i] = random.randint(0, remaining_power)
    else:
        computerAreas[i] = remaining_power
    remaining_power -= computerAreas[i]

# הדפסת טבלת תוצאות
print("\nתוצאות הקרב:")
print("אזור\tשחקן\tמחשב")
for i in range(4):
    print(f"{area_names[i]}\t{playerAreas[i]}\t{computerAreas[i]}")

# חישוב סך הכל כוח
playerTotalPower = sum(playerAreas)
computerTotalPower = sum(computerAreas)

# הכרזת מנצח
print("\nסיכום:")
print(f"כוח שחקן כולל: {playerTotalPower}")
print(f"כוח מחשב כולל: {computerTotalPower}")
if playerTotalPower > computerTotalPower:
    print("ניצחון השחקן!")
elif playerTotalPower < computerTotalPower:
    print("ניצחון המחשב!")
else:
    print("תיקו!")


