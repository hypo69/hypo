import random

def roll_dice():
    """מדמה הטלת שתי קוביות ומחזירה את סכומן."""
    dice1 = random.randint(1, 6) # קובייה ראשונה
    dice2 = random.randint(1, 6) # קובייה שנייה
    return dice1 + dice2 # מחזירה את סכום שתי הקוביות

def play_craps():
    """מנהלת את המשחק CRAPS."""
    print("ברוכים הבאים למשחק CRAPS!")

    # סיבוב ראשון
    sum_dice = roll_dice()
    print(f"הטלת הקוביות: {sum_dice}")

    # בדיקת תנאי ניצחון או הפסד בסיבוב הראשון
    if sum_dice in [7, 11]:
        print("ניצחת בסיבוב הראשון!")
        return # המשחק מסתיים
    elif sum_dice in [2, 3, 12]:
        print("הפסדת בסיבוב הראשון!")
        return # המשחק מסתיים
    
    # אם לא ניצחנו או הפסדנו, סכום הקוביות הופך ל"נקודה"
    point = sum_dice 
    print(f"הנקודה שלך היא {point}.")

    # המשך המשחק עד שמגיעים לנקודה או 7
    while True:
        sum_dice = roll_dice() # הטלה נוספת של הקוביות
        print(f"הטלת קוביות: {sum_dice}")

        if sum_dice == point:
            print("ניצחת! הגעת לנקודה שלך.")
            return # המשחק מסתיים
        elif sum_dice == 7:
            print("הפסדת! הטלת 7 לפני שהגעת לנקודה.")
            return # המשחק מסתיים

if __name__ == "__main__":
    play_craps()

